namespace HerPace.Core.Enums;

public enum TrainingStage
{
    Base = 0,       // Aerobic foundation, gradual mileage build
    Build = 1,      // Introduce tempo/interval work, volume increases
    Peak = 2,       // Highest volume and race-pace simulation
    Taper = 3       // Volume reduction, body consolidates fitness for race day
}
